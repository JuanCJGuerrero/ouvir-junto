function enterRoom(code) {
  if (!/^[A-Za-z0-9]{4,8}$/.test(code)) {
    homeError.textContent = t("invalidCode");
    return;
  }

  homeError.textContent = "";
  roomCode = code.toUpperCase();

  homeScreen.classList.add("hidden");
  roomScreen.classList.remove("hidden");
  document.getElementById("roomCodeDisplay").textContent = roomCode;
  history.replaceState(null, "", `?code=${roomCode}`);

  setUsername(currentUsername);

  resetPlayerSlot();
  player = null;
  playerReady = false;
  pendingState = null;
  endedHandledForCurrentVideo = false;

  ensurePlayerCreated();
  connectSocket();
}

function leaveRoom() {
  intentionalClose = true;
  clearTimeout(reconnectTimer);
  stopHeartbeat();
  closeSidebar();

  if (ws) {
    try {
      ws.close();
    } catch (e) {
      /* ignore */
    }
    ws = null;
  }

  if (player && player.destroy) {
    try {
      player.destroy();
    } catch (e) {
      /* ignore */
    }
  }
  player = null;
  playerReady = false;
  pendingState = null;
  endedHandledForCurrentVideo = false;
  resetPlayerSlot();

  playlist.length = 0;
  Object.keys(videoTitles).forEach((key) => delete videoTitles[key]);
  currentPlaylistIndex = -1;

  if (queueList) queueList.innerHTML = "";
  if (chatLog) chatLog.innerHTML = "";
  if (nowPlaying) nowPlaying.textContent = "";

  placeholder.classList.remove("hidden");
  syncOverlay.classList.add("hidden");
  roomError.textContent = "";

  roomCode = null;
  history.replaceState(null, "", window.location.pathname);

  roomScreen.classList.add("hidden");
  homeScreen.classList.remove("hidden");
}

