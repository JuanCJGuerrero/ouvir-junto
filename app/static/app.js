let currentLanguage = localStorage.getItem("language") || "pt";

function t(key, vars) {
  const dict = translations[currentLanguage] || translations.pt;
  let str = dict[key] ?? translations.pt[key] ?? key;
  if (vars) {
    Object.keys(vars).forEach((k) => {
      str = str.replace(`{${k}}`, vars[k]);
    });
  }
  return str;
}

// traduz as mensagens de sistema vindas do servidor (que chegam sempre em
// pt-BR); reconhece os 3 formatos fixos que o backend envia
function translateSystemMessage(text) {
  let m = text.match(/^(.+) entrou na sala$/);
  if (m) return t("sysJoined", { name: m[1] });

  m = text.match(/^(.+) saiu da sala$/);
  if (m) return t("sysLeft", { name: m[1] });

  m = text.match(/^(.+) agora é (.+)$/);
  if (m) return t("sysRenamed", { old: m[1], new: m[2] });

  return text;
}

