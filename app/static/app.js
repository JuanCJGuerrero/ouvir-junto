// ---------- WebSocket ----------

function connectSocket() {
  intentionalClose = false;

  const wsProtocol = location.protocol === "https:" ? "wss:" : "ws:";
  const nameForServer = displayName(currentUsername);
  const url = `${wsProtocol}//${location.host}/ws/${roomCode}?name=${encodeURIComponent(nameForServer)}`;

  ws = new WebSocket(url);

  ws.addEventListener("open", () => {
    reconnectAttempts = 0;
    roomError.textContent = "";
  });

  ws.addEventListener("message", (event) => {
    handleMessage(JSON.parse(event.data));
  });

  ws.addEventListener("close", () => {
    if (intentionalClose) return;

    stopHeartbeat();

    if (reconnectAttempts < 5) {
      reconnectAttempts += 1;
      roomError.textContent = t("reconnecting");
      reconnectTimer = setTimeout(connectSocket, 1200 * reconnectAttempts);
    } else {
      roomError.textContent = t("reconnectFailed");
    }
  });

  ws.addEventListener("error", () => {});
}

function send(msg) {
  if (ws && ws.readyState === WebSocket.OPEN) ws.send(JSON.stringify(msg));
}

// ---------- Mensagens recebidas ----------

function handleMessage(msg) {
  switch (msg.type) {
    case "sync":
      if (!playerReady) {
        pendingState = msg;
      } else {
        applySync(msg);
      }
      break;

    case "playlistSync":
      handlePlaylistSync(msg);
      break;

    case "userCount":
      updateUserCountDisplay(msg.count);
      break;

    case "chat":
      addChatMessage(msg.name || t("defaultUser"), msg.text || "", msg.clientId === clientId);
      break;

    case "system":
      addSystemMessage(translateSystemMessage(msg.text || ""));
      break;
  }
}

function applySync(msg) {
  applyingRemoteUpdate = true;

  if (!msg.videoId) {
    // sala sem vídeo: volta para o estado de "vazio" em vez de travar no último frame
    if (player && player.stopVideo) {
      try {
        player.stopVideo();
      } catch (e) {
        /* ignore */
      }
    }
    placeholder.classList.remove("hidden");
    syncOverlay.classList.add("hidden");
    applyingRemoteUpdate = false;
    return;
  }

  placeholder.classList.add("hidden");

  // OBS: já existiu aqui uma tentativa de compensar a latência de rede usando
  // o "serverTime" do servidor comparado com Date.now() do navegador. Isso foi
  // removido: o relógio do cliente e o do servidor não são sincronizados (sem
  // NTP garantido entre as duas máquinas), então qualquer diferença de horário
  // entre eles era somada à posição do vídeo, causando saltos de dezenas de
  // segundos em todos os players. Usamos a posição enviada pelo servidor
  // diretamente, que já é a posição correta calculada no momento do envio.
  const targetPosition = msg.position;

  const loadedId = player && player.getVideoData ? player.getVideoData().video_id : null;

  if (loadedId !== msg.videoId) {
    endedHandledForCurrentVideo = false;
    player.loadVideoById(msg.videoId, targetPosition);
    if (!msg.isPlaying) player.pauseVideo();
  } else {
    const drift = Math.abs(player.getCurrentTime() - targetPosition);
    if (drift > 1.2) player.seekTo(targetPosition, true);

    const state = player.getPlayerState();
    if (msg.isPlaying && state !== YT.PlayerState.PLAYING && state !== YT.PlayerState.BUFFERING) {
      player.playVideo();
    }
    if (!msg.isPlaying && state === YT.PlayerState.PLAYING) {
      player.pauseVideo();
    }
  }

  // o próximo heartbeat deve comparar a posição a partir do ponto que
  // acabamos de aplicar, e não da posição anterior à correção — caso
  // contrário o salto da própria correção seria lido como um "seek manual"
  resetHeartbeatBaseline(targetPosition);

  // se o navegador bloquear o autoplay, avisa e deixa o usuário liberar com um clique
  clearTimeout(syncCheckTimer);
  if (msg.isPlaying) {
    syncCheckTimer = setTimeout(() => {
      if (!player) return;
      const state = player.getPlayerState();
      if (state !== YT.PlayerState.PLAYING && state !== YT.PlayerState.BUFFERING) {
        syncOverlay.classList.remove("hidden");
      }
    }, 1200);
  } else {
    syncOverlay.classList.add("hidden");
  }

  // janela um pouco maior que a anterior (era 400ms): dá tempo do player
  // realmente assumir o estado pedido (ex.: buffering) antes de voltarmos a
  // tratar mudanças de estado como ações locais do usuário
  setTimeout(() => (applyingRemoteUpdate = false), 800);
}

