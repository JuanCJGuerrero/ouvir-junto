// ---------- YouTube player ----------

// chamado automaticamente pela API do YouTube quando ela carrega
function onYouTubeIframeAPIReady() {
  ytApiReady = true;
  ensurePlayerCreated();
}

function ensurePlayerCreated() {
  if (player || !ytApiReady || !document.getElementById("player")) return;

  player = new YT.Player("player", {
    height: "360",
    width: "640",
    playerVars: { playsinline: 1 },
    events: {
      onReady: () => {
        playerReady = true;
        if (pendingState) {
          applySync(pendingState);
          pendingState = null;
        }
      },
      onStateChange: onPlayerStateChange,
    },
  });
}

function onPlayerStateChange(event) {
  // todo cliente observa o próprio drift enquanto está tocando, não só quem
  // deu o play originalmente — assim a sincronia não depende de uma única
  // aba continuar enviando heartbeat (ex.: se ela for para segundo plano)
  if (event.data === YT.PlayerState.PLAYING) {
    syncOverlay.classList.add("hidden");
    startHeartbeat();
    endedHandledForCurrentVideo = false;
  } else if (event.data === YT.PlayerState.PAUSED || event.data === YT.PlayerState.ENDED) {
    stopHeartbeat();
  }

  if (applyingRemoteUpdate) return;

  if (event.data === YT.PlayerState.PLAYING) {
    send({ type: "play", position: player.getCurrentTime() });
  } else if (event.data === YT.PlayerState.PAUSED) {
    send({ type: "pause", position: player.getCurrentTime() });
  } else if (event.data === YT.PlayerState.ENDED) {
    // avança para o próximo vídeo da fila quando o vídeo termina naturalmente;
    // como todos os players sincronizados chegam ao fim quase juntos, vários
    // clientes disparam isso ao mesmo tempo — o servidor ignora os pedidos
    // repetidos (ver ADVANCE_DEBOUNCE_SECONDS no backend)
    if (!endedHandledForCurrentVideo) {
      endedHandledForCurrentVideo = true;
      if (playlist.length > 0) {
        send({ type: "playlistNext" });
      }
    }
  }
}

// ---------- Heartbeat / detecção de avanço manual (seek) ----------

let lastHeartbeatTime = null;
let lastHeartbeatPos = null;

function resetHeartbeatBaseline(position) {
  lastHeartbeatTime = Date.now();
  lastHeartbeatPos = position;
}

function startHeartbeat() {
  stopHeartbeat();
  resetHeartbeatBaseline(player.getCurrentTime());

  heartbeatTimer = setInterval(() => {
    if (!player || applyingRemoteUpdate) return;

    const now = Date.now();
    const pos = player.getCurrentTime();
    const expectedPos = lastHeartbeatPos + (now - lastHeartbeatTime) / 1000;
    const jumped = Math.abs(pos - expectedPos) > 1.5;

    lastHeartbeatTime = now;
    lastHeartbeatPos = pos;

    // se detectar um salto (usuário arrastou a barra), avisa os outros mais rápido
    send({ type: jumped ? "seek" : "heartbeat", position: pos });
  }, 2000);
}

function stopHeartbeat() {
  if (heartbeatTimer) clearInterval(heartbeatTimer);
  heartbeatTimer = null;
}

// ---------- Playlist ----------

async function handlePlaylistSync(msg) {
  playlist.length = 0;

  for (const videoId of msg.playlist) {
    playlist.push(videoId);
    if (!videoTitles[videoId]) {
      fetchTitle(videoId); // não bloqueia a renderização da fila
    }
  }

  currentPlaylistIndex = msg.currentIndex;
  renderPlaylist();
}

async function fetchTitle(videoId) {
  try {
    const response = await fetch(
      `https://www.youtube.com/oembed?url=https://www.youtube.com/watch?v=${videoId}&format=json`
    );
    const data = await response.json();
    videoTitles[videoId] = data.title;
  } catch {
    videoTitles[videoId] = videoId;
  }
  renderPlaylist();
}

function renderPlaylist() {
  if (!queueList) return;

  queueList.innerHTML = "";

  playlist.forEach((videoId, index) => {
    const li = document.createElement("li");
    li.className = "queue-item" + (index === currentPlaylistIndex ? " playing" : "");

    const title = videoTitles[videoId] || t("videoFallback", { n: index + 1 });
    const meta = index === currentPlaylistIndex ? t("nowPlayingBadge") : `#${index + 1}`;

    li.innerHTML = `
      <img src="https://img.youtube.com/vi/${videoId}/mqdefault.jpg">
      <div class="queue-item-info">
        <div class="queue-item-title">${escapeHtml(title)}</div>
        <div class="queue-item-meta">${meta}</div>
      </div>
      <button class="queue-item-remove" type="button" title="${escapeHtml(t("removeTooltip"))}">✕</button>
    `;

    li.addEventListener("click", (e) => {
      if (e.target.classList.contains("queue-item-remove")) return;
      playPlaylistVideo(index);
    });

    li.querySelector(".queue-item-remove").addEventListener("click", (e) => {
      e.stopPropagation();
      send({ type: "playlistRemove", index });
    });

    queueList.appendChild(li);
  });

  updateNowPlaying();
}

function updateNowPlaying() {
  if (!nowPlaying) return;

  if (currentPlaylistIndex >= 0 && playlist[currentPlaylistIndex]) {
    const vid = playlist[currentPlaylistIndex];
    nowPlaying.innerHTML = `${escapeHtml(t("nowPlayingPrefix"))}<strong>${escapeHtml(videoTitles[vid] || vid)}</strong>`;
  } else {
    nowPlaying.textContent = "";
  }
}

function playPlaylistVideo(index) {
  send({ type: "playlistPlay", index });
}

